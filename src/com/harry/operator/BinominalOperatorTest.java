package com.harry.operator;

public class BinominalOperatorTest {
	public static void main(String[] args) {
		int a = 11;
		int b = 2;

		int result01 = a + b;
		int result02 = a - b;
		int result03 = a * b;
		int result04 = a / b;
		int result05 = a % b;

		System.out.println("a+b value is " + result01);
		System.out.println("a-b value is " + result02);
		System.out.println("a*b value is " + result03);
		System.out.println("a/b value is " + result04);
		System.out.println("a%b value is " + result05);
	}
}
